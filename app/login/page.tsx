export default function LoginPage() {
  const router = useRouter();
  const supabase = createClient();

  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);

  async function handleLogin(e: React.FormEvent) {
    e.preventDefault();
    setError(null);
    setLoading(true);

    const { error } = await supabase.auth.signInWithPassword({
      email,
      password,
    });

    setLoading(false);

    if (error) {
      setError(error.message);
      return;
    }

    router.push("/dashboard");
    router.refresh();
  }

  async function handleGoogleLogin() {
    setError(null);
    const { error } = await supabase.auth.signInWithOAuth({
      provider: "google",
      options: {
        redirectTo: `${process.env.NEXT_PUBLIC_SITE_URL}/auth/callback`,
      },
    });
    if (error) setError(error.message);
  }

  return (
    <main className="auth-page">
      <div className="badge-card">
        <div className="dash-card-logo">
          <Logo size="md" />
        </div>
        <div className="badge-eyebrow mono">
          <span className="status-dot" />
          Secure Access
        </div>
        <h1 className="badge-title">Welcome back</h1>
        <p className="badge-subtitle">
          Scan in with your credentials to reach your dashboard.
        </p>

        <form onSubmit={handleLogin}>
          <div className="field">
            <label htmlFor="email">Email</label>
            <input
              id="email"
              type="email"
              placeholder="you@example.com"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              required
            />
          </div>
          <div className="field">
            <label htmlFor="password">Password</label>
            <input
              id="password"
              type="password"
              placeholder="••••••••"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              required
            />
          </div>

          {error && <p className="error-text">{error}</p>}

          <button type="submit" className="btn-primary" disabled={loading}>
            {loading ? "Verifying..." : "Log In"}
          </button>
        </form>

        <div className="divider">Or continue with</div>

        <button className="btn-google" onClick={handleGoogleLogin}>
          <GoogleIcon />
          Google
        </button>

        <p className="footer-note">
          No badge yet? <Link href="/signup">Create one</Link>
        </p>

        <div className="barcode">
          <div className="barcode-bars" />
          <div className="barcode-serial mono">NO. 001-LOGIN-2026</div>
        </div>
      </div>
    </main>
  );
}
